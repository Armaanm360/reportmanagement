                    <h5>6.2	Bangladesh Bank (BB)</h5>
                    <h5>6.2.1	Recent Achievements: According to the present government's 'Vision 2021', the rate of poverty has been expressed below 15 percent. In order to achieve this goal, Bangladesh Bank has taken steps to disburse a large amount of credit to agriculture and small and medium industries as a short-term step, which will help in forming long-term strategies in the near future. Bangladesh Bank has recently formulated strategic plans for achieving some main objectives. The main achievements are: (1) The private sector credit growth stood at 9.71 percent during the period of 2017-18; (2) Due to the reduction in credit interest rates in the first half of current fiscal year, the interest spread has decreased from 4.62 percent in July, 2017 to 4.44 percent by the end of December 2017; (3) To bring down the gap between average interest rate below 5 percent in the Monotary Policy of the second half of financial year 2017-18, the average interest spread of loans and deposits in all sectors (except credit card and consumer credit) at the end of January, 2018, was 4.34 percent.. 4) The target for distribution of agriculture and rural credit in the financial year 2017-18 had been fixed at Tk 20,400.00 crore, which was 16.24 percent higher than the target of previous year's budget of 17,550.00 crore; (5) The amount of loans distributed by scheduled banks till February of the fiscal year 2007-18 stands at Tk 14,520.42 crore, which is 71.18 percent of the target. At the same time, the amount of recovery of agricultural and rural credit has increased to 13,596.99 crores; (6) In February 2016, a new merchandise for long term refinancing facility has been introduced by Bangladesh Bank for 200 Million US Dollars named "GTF"</h5>
