                    <h5>6.3	Bangladesh Securities and Exchange Commission (BSEC)</h5>
                    <h5>6.3.1	Recent Achievements: In the last three financial years from 2015-2016 to 2017-18, a total of TK. 60,814.77 crore has been approved by the Commission to raise capital by different issuers and instruments to invest in the capital market; among those, through initial public offering (IPO) of 27 companies, rights issue of 10 companies, debenture and bond issue of 54 companies, Preference share issues of 18 companies, increase of capital of 324 private and 217 public limited companies. Additionally, in this same timeframe, a total of TK. 1,636.32 crore has been raised to invest in the capital market by 31 open-end mutual funds and 7 close-end mutual funds. The aforesaid raising of capital by different issuers and also raising of fund by different mutual funds are contributing in industrialization and creation of employment as well as overall economic development of the country. To keep effective contribution towards increased supply of good securities, the Commission has provided registration certificates to 8 stock brokers, 9 stock dealers, 3 asset managers, 21 security custodians, 21 trustees for debt securities, 10 depository participants, 2 trustees and 7 fund managers in FY 2017-18. Significant and massive reform initiatives have been taken; among others, enhancing efficiency and strengthening the role of the Commission, demutualization of the stock exchanges, establishment of the special tribunal for the capital market, reform of capital market related laws, making of new rules and regulations and elevating the country’s reputation in international arena, etc. As on June, 2018, number of listed securities in the Stock Exchanges stood at 572 including 305 listed companies. </h5>
