                    <h5>6.4	Bangladesh Institute of Capital Market (BICM)</h5>
                    <h5>6.4.1	Recent Achievements: Training has been provided to the officials of Bangladesh Securities and Exchange Commission (BSEC), Stock Exchange and Intermediary Institutions for achieving the excellence of theoretical and practical knowledge about capital market. Meanwhile, BICM has conducted different training Schemes including (1) Investors Education Scheme (2) Certificate in securities’ laws in Bangladesh (3) Certificate in financial statement analysis (4) Certificate in international financial reporting standard (5) Enterprise network solutions for stock exchange members (6) Certificate in corporate governance and addressing fraudulence, abuse, market manipulation and insider trading (7) Certificate in investment analysis and securities valuation (8) Certificate in financial journalism (9) Technical Analysis (10) Capital Reaching and Investment in Primary Market (11) Post Graduate Diploma in Capital Market  etc. So far 10,415 persons have been trained and 17 seminar/workshops have been organized. The Institute has already started a day-long program for newly graduated graduates interested in becoming a professional in the capital market along with the evening PGDCM program.</h5>
